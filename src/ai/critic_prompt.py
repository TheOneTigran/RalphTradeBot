"""
critic_prompt.py — Промпт для второго LLM-прохода (Critic).

Задача Критика:
  Получить черновой TradePlan от Actor-а и проверить его на нарушение
  жёстких правил Elliott Wave (89WAVES). Не анализировать рынок заново —
  только выявлять логические противоречия в разметке.
"""

CRITIC_SYSTEM_PROMPT = """\
Ты — строжайший аудитор торговых планов по методологии 89WAVES.
Ты НЕ анализируешь рынок. Ты ТОЛЬКО проверяешь корректность волновой разметки.

ТВОЯ ЗАДАЧА: найти нарушения жёстких правил Эллиотта в предоставленном плане.

═══════════════════════════════════════════
ЧЕКЛИСТ ОБЯЗАТЕЛЬНЫХ ПРОВЕРОК:
═══════════════════════════════════════════

[ИМПУЛЬС]:
□ Волна 2 не зашла за старт 1? (НАРУШЕНИЕ = инвалидация)
□ Волна 3 прорвала конец 1? (НАРУШЕНИЕ = инвалидация)
□ Волна 3 не является самой короткой из 1, 3, 5? (НАРУШЕНИЕ = инвалидация)
□ Волна 4 не пересеклась с зоной 1? (НАРУШЕНИЕ = инвалидация)

[ДИАГОНАЛЬ]:
□ Волна 4 заходит в зону волны 2? (ОТСУТСТВИЕ = не диагональ)
□ Линии 1-3 и 2-4 сходятся? (НАРУШЕНИЕ = не диагональ)

[ЗИГЗАГ]:
□ Волна B < начала A? (НАРУШЕНИЕ = инвалидация)
□ Волна C вышла за конец A? (ОТСУТСТВИЕ = не зигзаг, а плоскость)

[ПЛОСКОСТЬ]:
□ Волна B >= 90% от A? (НАРУШЕНИЕ = это не плоскость)
□ Волна B <= 138% от A? (ПРЕВЫШЕНИЕ = другая расширенная структура)

[ТРЕУГОЛЬНИК]:
□ Каждая следующая волна ABCDE не пробивает предыдущую? (НАРУШЕНИЕ = инвалидация)

[РИСК/ДОХОДНОСТЬ И ВХОД]:
□ Risk/Reward >= 1:1.5? (МИНИМАЛЬНЫЙ ПОРОГ — предупредить если меньше)
□ Стоп-лосс стоит ЗА уровнем инвалидации, не перед ним?
□ ОПРАВДАННОСТЬ СДЕЛКИ: Если это не WAIT, действительно ли на графике есть железобетонный сетап (свипы, кластеры, POC, дивергенции)? Если условия посредственные, напиши в critical_errors: "Требуется режим WAIT. Отмените сделку и дайте зоны ожидания."

[ИЕРАРХИЯ И КООРДИНАТЫ ВОЛН]:
□ Используется ли классическая нотация 89WAVES/Elliott (1D: (1) / (A), 4H: 1 / A, 1H: [i] / [a], 15m: (i) / (a))? (НАРУШЕНИЕ = невалидно)
□ Указан ли массив `waves_breakdown` с start_price и end_price для каждой описанной волны? (НАРУШЕНИЕ = инвалидация)

═══════════════════════════════════════════
ФОРМАТ ОТВЕТА — СТРОГО JSON:
═══════════════════════════════════════════
{
  "is_valid": true или false,
  "critical_errors": ["список критических нарушений, инвалидирующих разметку"],
  "warnings": ["список предупреждений (не критично, но стоит учесть)"],
  "corrected_wave_label": "Скорректированная маркировка (если нашёл ошибки, иначе null)",
  "confidence_boost": "Краткое пояснение: почему разметка корректна (если is_valid=true)"
}
"""


def build_critic_messages(draft_plan_json: str, market_summary: str) -> list[dict]:
    """
    Формирует сообщения для Критика.

    Args:
        draft_plan_json:  JSON-строка чернового плана от Actor-а.
        market_summary:   Краткая выжимка ключевых векторов и уровней.

    Returns:
        Список сообщений для LLM API.
    """
    user_content = (
        f"Проверь этот торговый план на нарушения правил 89WAVES:\n\n"
        f"```json\n{draft_plan_json}\n```\n\n"
        f"Краткий контекст рынка (для проверки цифр):\n{market_summary}\n\n"
        f"Верни результат аудита строго в JSON формате."
    )
    return [
        {"role": "system", "content": CRITIC_SYSTEM_PROMPT},
        {"role": "user",   "content": user_content},
    ]


def build_correction_messages(
    draft_plan_json: str,
    critic_feedback_json: str,
    market_data_json: str,
) -> list[dict]:
    """
    Формирует сообщения для второго прохода Actor-а (исправление).

    Args:
        draft_plan_json:      Черновой план Actor-а.
        critic_feedback_json: Ответ Критика с ошибками.
        market_data_json:     Полные рыночные данные (из LLMContext).

    Returns:
        Список сообщений для LLM API.
    """
    from src.ai.ai_prompt_builder import SYSTEM_PROMPT

    user_content = (
        f"Твой предыдущий анализ получил замечания от аудитора:\n\n"
        f"ЧЕРНОВОЙ ПЛАН:\n```json\n{draft_plan_json}\n```\n\n"
        f"ЗАМЕЧАНИЯ АУДИТОРА:\n```json\n{critic_feedback_json}\n```\n\n"
        f"ИСПРАВЬ план, устранив все критические ошибки. "
        f"Рыночные данные для переработки:\n\n{market_data_json}\n\n"
        f"Верни исправленный план строго в JSON формате."
    )
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user",   "content": user_content},
    ]
