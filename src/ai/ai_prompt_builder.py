def build_messages(context_data: LLMContext) -> list[dict]:
    """
    Собирает список сообщений для вызова LLM API (формат OpenAI Chat).

    Args:
        context_data: Объект LLMContext с векторами по всем таймфреймам.

    Returns:
        Список сообщений: [{'role': ..., 'content': ...}]
    """
    json_payload = context_data.model_dump_json(indent=2)

    # Формируем краткую шапку с сигналами-предупреждениями для LLM
    clusters_summary = []
    for tf_data in context_data.timeframes:
        for cl in tf_data.fib_clusters[:3]:           # топ-3 кластера по ТФ
            tag = "🔴 СОПР" if not cl.is_support else "🟢 ПОДД"
            clusters_summary.append(
                f"  [{tf_data.timeframe}] {tag} {cl.price:.2f} (x{cl.strength}): "
                + ", ".join(cl.levels[:2])
            )

    cluster_block = ""
    if clusters_summary:
        cluster_block = (
            "⚡ КЛЮЧЕВЫЕ КЛАСТЕРЫ ФИБОНАЧЧИ (используй при определении TP и SL):\n"
            + "\n".join(clusters_summary)
            + "\n\n"
        )

    user_content = (
        f"Проведи профессиональный фрактальный волновой анализ для **{context_data.symbol}**.\n\n"
        f"{cluster_block}"
        f"Полные данные (от старшего ТФ к младшему):\n\n"
        f"```json\n{json_payload}\n```\n\n"
        f"Верни торговый план строго в JSON формате. Никакого текста вне JSON."
    )

    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user",   "content": user_content},
    ]
